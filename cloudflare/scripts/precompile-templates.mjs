// Precompile the Nunjucks templates to plain JS at build time.
//
// The Cloudflare Workers runtime forbids eval / new Function ("Code generation
// from strings disallowed"), and Nunjucks compiles templates from strings at
// runtime. Precompiling here turns each template into a static JS function so
// the Worker never compiles at runtime. Output: src/templates.generated.ts.
//
// Run via `npm run precompile` (wired into pretest/pretypecheck and the wrangler
// [build] command).

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import nunjucks from "nunjucks";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.resolve(HERE, "..", "src");
const TPL = path.join(SRC, "templates");

// Discover every template under src/templates/{bird,vyos}. The registry NAME is
// the path relative to its set dir — exactly what `{% include "..." %}` and
// `env.render("...")` use (e.g. "header.bird.njk", "defaults/prefix_lists.bird").
// birds.py isn't .njk/.bird, so it's naturally excluded.
function collect(setDir) {
  const root = path.join(TPL, setDir);
  return fs
    .readdirSync(root, { recursive: true })
    .map((f) => String(f).split(path.sep).join("/"))
    .filter((f) => f.endsWith(".njk") || f.endsWith(".bird"))
    .sort()
    .map((name) => [name, `${setDir}/${name}`]);
}

const TEMPLATES = [...collect("bird"), ...collect("vyos")];

// REQUIRED for byte-identical output: compensate for two whitespace behaviours
// Jinja2 has but Nunjucks lacks (no config flag exists for either):
//  1. Jinja trim_blocks strips the newline after a comment `#}`; Nunjucks
//     trimBlocks does not — without this, comment-only lines emit a blank line.
//  2. Jinja keep_trailing_newline=False drops one trailing newline per template;
//     Nunjucks keeps it — without this, {% include %} boundaries gain a newline.
// Applied to the source before precompile so the behaviour is baked into the
// compiled function.
function preprocess(src) {
  src = src.replace(/#\}\n/g, "#}");
  if (src.endsWith("\n")) src = src.slice(0, -1);
  return src;
}

const env = new nunjucks.Environment(null, {
  autoescape: false,
  trimBlocks: true,
  lstripBlocks: true,
});

const snippets = [];
for (const [name, rel] of TEMPLATES) {
  const src = preprocess(fs.readFileSync(path.join(TPL, rel), "utf8"));
  snippets.push(nunjucks.precompileString(src, { name, env }));
}

const out =
  "// @ts-nocheck\n" +
  "// AUTO-GENERATED by scripts/precompile-templates.mjs — do not edit.\n" +
  "// Run `npm run precompile` to regenerate.\n" +
  "const window = { nunjucksPrecompiled: {} };\n" +
  snippets.join("\n") +
  "\nexport default window.nunjucksPrecompiled;\n";

fs.writeFileSync(path.join(SRC, "templates.generated.ts"), out);
console.log(`precompiled ${TEMPLATES.length} templates -> src/templates.generated.ts`);
